from absl import flags

FLAGS = flags.FLAGS

def patchable(point_rc, image):
    return point_rc[0] >= FLAGS.depth and \
    point_rc[0] < image.shape[0] - FLAGS.depth and \
    point_rc[1] >= FLAGS.depth and \
    point_rc[1] < image.shape[1] - FLAGS.depth
